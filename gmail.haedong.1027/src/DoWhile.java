
public class DoWhile {
	public static void main(String[] args) {
		
		int i = 1;
		do {
			System.out.println("hani" + i + ".jpg");
			i += 1;
			
		}while(i < 4);
		
		System.out.println("============================");
		
		do {
			System.out.println("hani" + (i-2) + ".jpg");
			i -= 1;
		}while(i > 1);
		
		System.out.println("============================");
		
		//5, 3, 1 출
		// 1, 2, 3
		// 5, 3, 1
		int j = 1;
		do {
			System.out.println((-2*j)+7);
			j += 1;
		}while (j < 4);
		
		
		System.out.println("============================");
		//문제가 뭔지 기억이 안
		
		int k = 1;
		
		
	
	}
}
